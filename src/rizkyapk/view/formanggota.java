    public void tableanggota(){
        
        Object header[]={"NTA","Nama","Gugus Depan","Status"}; 
        DefaultTableModel data=new DefaultTableModel(null, header);
        
        List<Pramuka> listagt=new java.util.ArrayList<>();
        listagt=agtctrl.findPramukaEntities();
        
        if (listagt.size()==0){
            tAnggota.setModel(data);
        }else{
            for(int i=0;i<listagt.size();i++){
                agt=listagt.get(i);
                String row [] ={
                    agt.getNta(),
                    agt.getNama(),
                    agt.getGudep(),
                    agt.getGolongan(),
                };
                data.addRow(row);
                tAnggota.setModel(data);
            }
        }
    }

