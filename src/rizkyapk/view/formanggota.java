    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        String golongan;
        golongan = (String) cbGolongan.getSelectedItem();
        
        agt.setNta(txtNTA.getText());
        agt.setNama(txtNama.getText());
        agt.setGudep(txtGudep.getText());
        agt.setGolongan(golongan);
        
        try {
            agtctrl.create(agt);
            JOptionPane.showMessageDialog(null, "Data Tersimpan");
            tableanggota();
            
            
            txtNTA.setText("");
            txtNama.setText("");
            txtGudep.setText("");
            cbGolongan.setSelectedIndex(0);
            
            
        } catch (Exception ex) {
            Logger.getLogger(formanggota.class.getName()).log(Level.SERVE, null, ex);
        }
    }//GEN-LAST:event_btSimpanActionPerformed

